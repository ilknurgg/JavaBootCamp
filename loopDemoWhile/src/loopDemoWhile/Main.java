package loopDemoWhile;

public class Main {

	public static void main(String[] args) {
	    //For loop
		for(int i=2; i<10; i+=2)
		{
			
			System.out.println(i);
		}
       System.out.println("For Döngüsü bitti");
       
       int i=2;
       //While loop
       while(i<10) {
    	   System.out.println(i);
    	   i+=2;
       }
       
       System.out.println("While Döngüsü bitti");
	}

}


